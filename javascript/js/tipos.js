// String
let cadena = "Distrital";
console.log(cadena);
console.log(typeof cadena);

// Number
let entero = 21;
console.log(entero);
console.log(typeof entero);
let real = 14.56;
console.log(real);
console.log(typeof real);

// Boolean -> true | false
let valor = false;
console.log(valor);
console.log(typeof valor);

// undefined
let noDefinido;
console.log(noDefinido);
console.log(typeof noDefinido);

// null -> object
let ninguno = null;
console.log(ninguno);
console.log(typeof ninguno);
