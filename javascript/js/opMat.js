// Suma de constantes
let suma = 5 + 4;
console.log(suma);

// suma de constantes y variables
let resSuma = suma + 10;
console.log(resSuma);

// No modifica la variable
console.log(resSuma + 20);

// Shortcuts de incremento/decremento
resSuma++;
console.log(resSuma);
resSuma--;
console.log(resSuma);
resSuma += 6;
console.log(resSuma);
resSuma -= 10;
console.log(resSuma);

// Operador de resta
let resta = resSuma - 10;
console.log(resta);

// Multiplicación
let multiplicacion = resta * 2;
console.log(multiplicacion);

// División
let division = multiplicacion / 3;
console.log(division);

// Residuo (módulo)
let residuo = 20 % 3; // Sobra 2
division = 20 / 3; // 6.66666
console.log(residuo);
console.log(division);
